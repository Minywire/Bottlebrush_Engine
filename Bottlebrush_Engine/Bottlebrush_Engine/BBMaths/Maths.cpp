#include "Maths.h"

namespace BBMaths {

float Cos(float x) { return std::cos(x); }

float Sin(float x) { return std::sin(x); }

float Tan(float x) { return std::tan(x); }

float Sqrt(float x) { return std::sqrt(x); }

float InverseSqrt(float x) { return 1.0f / std::sqrt(x); }

}  // namespace BBMaths
